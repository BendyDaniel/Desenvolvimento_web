function somar(n1,n2){
    return n1+n2
}
function subtrair(n1,n2){
    return n1-n2
}
function dividir(n1,n2){
    return n1/n2
}
function multiplicar(n1,n2){
    return n1*n2
}

var soma = somar(6,8)
console.log(soma )

var subtrai = subtrair(6,2)
console.log(subtrai )

var dividi = dividir(900,9)
console.log(dividi )

var multiplica = multiplicar(100,5)
console.log(multiplica )