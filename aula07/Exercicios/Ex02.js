var adicao = (n1, n2) => {
    return n1 + n2
}
var sutracao = (n1, n2) => {
    return n1 - n2
}
var divisao = (n1, n2) => {
    return n1 / n2
}
var mutiplicar = (n1, n2) => {
    return n1 * n2
}
 
console.log (adicao(9,9))

console.log (sutracao(88,35))

console.log (divisao(88,8))

console.log (mutiplicar(12,12))

